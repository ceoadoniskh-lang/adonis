export const getItemsByCategories = (categoryKeys: string[]): CatalogItem[] => {
  if (categoryKeys.length === 0 || categoryKeys.includes("all")) {
    return catalogItems;
  }
  return catalogItems.filter((item) => categoryKeys.includes(item.category));
};

export const getItemById = (id: string): CatalogItem | undefined => {
  return catalogItems.find((item) => item.id === id);
};
